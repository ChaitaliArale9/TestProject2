package com.amitsir.complexpattern;

public class Diagonal5 {

	public static void main(String[] args) {
     int i,j;
  for(i=1;i<=3;i++)
  {
	  for(j=1;j<=3;j++)
	  {
		  if(i==j) {
		  System.out.print("5");
		  }
		  else
		  {
			  System.out.print("0");

		  }
			  
	  }
	  System.out.println("");

  }
  
	}

}
