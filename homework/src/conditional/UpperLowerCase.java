package conditional;

public class UpperLowerCase {

	public static void main(String[] args) {
		int alfa= 67;
		
		if(alfa>=66 && alfa<=96)
		{
			System.out.println("Is Lower Case");

		}

		else if(alfa>=97 && alfa<=123)
		{
			System.out.println("Is Upper Case");

		}
		if(alfa>=48 && alfa<=57)
		{
			System.out.println("Digit");

		}


	}

}
